// Написати програму яка перевіряє чи введене число потрапляє в
// діапазон від 55 до 99 включно

let Numb = prompt('Введите число');

if (Numb){
if(!Number.isInteger(Numb*1)){ alert('не число!');}
else {
    if ((Numb*1 > 55) && (Numb*1 < 100)){alert('Ok')}
    else {alert('Не входит в диапазон от 55 до 99')}
}
} else alert('Отменено пользователем!')